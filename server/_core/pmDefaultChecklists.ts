// — لو الفني غيّر اسم القسم عن الأسماء القياسية، ما راح تطلع له اقتراحات
// (سلوك آمن: نتجنب اقتراحات خاطئة بدل تخمين تطابق جزئي).
export function getDefaultChecklistFor(title: string, frequency: string): string[] {
  const section = DEFAULT_CHECKLISTS[title];
  if (!section) return [];
  return section[frequency as PMFrequencyKey] ?? [];
}
